    private void table_update() {
        int CC;
        try {
            ResultSet Rs = DbUtil.getQueryResult(props.getProperty("productsquery"), con);

            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
            jTable1.setEnabled(false);
            jTable1.setRowHeight(30);

            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();

                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getInt("productid"));
                    v2.add(Rs.getString("productname"));
                    v2.add(Rs.getDouble("price"));
                    v2.add(Rs.getInt("quantity"));
                    v2.add(Rs.getString("barcodenum"));
                    v2.add(Rs.getString("description"));
                    v2.add(Rs.getString("unreason"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


