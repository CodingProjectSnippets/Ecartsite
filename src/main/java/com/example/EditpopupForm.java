    public boolean Validateform() {
        StringBuilder errorText = new StringBuilder();
        errorText.append(checkTestbox(jTextField1, false));
        errorText.append(checkTestbox(jTextField2, true));
        errorText.append(checkTestbox(jTextField4, true));
        errorText.append(checkTestbox(jTextField6, false));
        errorText.append(checkTestbox(jTextField7, false));
        errorText.append(checkTestbox(jTextField8, false));
        System.out.println(errorText.length() + "sandeep length");

        return errorText.length() == 0;

    }

    public String checkTestbox(JTextField textField, boolean isnum) {

        String reason = "";
        if (textField.getText().length() == 0) {
            //textField.setBackground(Color.red);
            textField.setBorder(new LineBorder(Color.red, 1));
            reason = "Textfield 1 is mandatory";
        } else {
            if (isnum) {
                if (!isNumeric(textField.getText())) {
                    textField.setBorder(new LineBorder(Color.red, 1));
                    JOptionPane.showMessageDialog(this, "Please Ener numeric Fields");
                } else {
                    textField.setBorder(new LineBorder(Color.GREEN, 1));
                }
            } else {
                textField.setBorder(new LineBorder(Color.GREEN, 1));
            }

        }

        return reason;
    }

    public static boolean isNumeric(String strNum) {
        try {
            Double.parseDouble(strNum);
        } catch (NumberFormatException nfe) {
            return false;
        }
        return true;
    }

