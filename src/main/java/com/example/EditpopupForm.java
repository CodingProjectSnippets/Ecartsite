        newbean.setProductname(jTextField1.getText());
        newbean.setPrice(praseStr2Double(jTextField2.getText()));
        newbean.setQuantity(praseStr2Int(jTextField4.getText()));
        newbean.setBarcode(jTextField6.getText());
